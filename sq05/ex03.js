let ultimoNumeroDaPlaca = prompt("Digite o último número da placa do seu carro: ")
if (ultimoNumeroDaPlaca == 0 || ultimoNumeroDaPlaca == 1) {
    alert("Não pode digirir na segunda-feira")
} else if (ultimoNumeroDaPlaca == 2 || ultimoNumeroDaPlaca == 3) {
    alert("Não pode digirir na terça-feira")
} else if (ultimoNumeroDaPlaca == 4 || ultimoNumeroDaPlaca == 5) {
    alert("Não pode digirir na quarta-feira")
} else if (ultimoNumeroDaPlaca == 6 || ultimoNumeroDaPlaca == 7) {
    alert("Não pode digirir na quinta-feira")
} else if (ultimoNumeroDaPlaca == 8 || ultimoNumeroDaPlaca == 9) {
    alert("Não pode digirir na sexta-feira")
}